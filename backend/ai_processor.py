import json
from groq import Groq

def parse_price_list_with_ai(raw_text: str, api_key: str):
    if not api_key:
        raise ValueError("Не указан API-ключ Groq.")
    
    client = Groq(api_key=api_key)
    
    # Берем кусок текста (чтобы не упереться в лимит токенов для прототипа)
    text_chunk = raw_text[:12000] 

    system_prompt = """Ты главный эксперт-аналитик по медицинским данным и биллингу. Твоя задача — профессионально извлечь и нормализовать прайс-лист клиники из хаотичного сырого текста, приведя его к стандартам Номенклатуры медицинских услуг.
ОТВЕТЬ ТОЛЬКО В ФОРМАТЕ JSON (массив объектов). Без markdown-оберток и лишних слов.

СЛОВАРЬ МЕДИЦИНСКИХ СОКРАЩЕНИЙ (Обязательно разворачивай их в standardized_name):
- ОАК -> Общий анализ крови
- ОАМ -> Общий анализ мочи
- БХ -> Биохимический анализ крови
- ЭКГ -> Электрокардиография
- ЭЭГ -> Электроэнцефалография
- ЭХО-КГ -> Эхокардиография
- ФГДС / ЭГДС -> Эзофагогастродуоденоскопия
- МРТ -> Магнитно-резонансная томография
- КТ -> Компьютерная томография
- УЗИ / УЗДС -> Ультразвуковое исследование / Ультразвуковое дуплексное сканирование
- конс. / осмотр -> консультация
- перв. / повт. -> первичная / повторная
- орг. / ж.п. / поджелуд. ж. -> органов / желчного пузыря / поджелудочной железы
- в/в, в/м, п/к -> внутривенное, внутримышечное, подкожное введение

РАСШИРЕННЫЕ КАТЕГОРИИ (Обязательно выбери наиболее точную. КАТЕГОРИЯ НЕ МОЖЕТ БЫТЬ ПУСТОЙ):
1. "Приемы и консультации" (Аллерголог, Терапевт, Гинеколог, Хирург и т.д.)
2. "Лабораторная диагностика" (Анализы крови, мочи, кала, мазки, ПЦР, ИФА)
3. "Ультразвуковая диагностика" (УЗИ любых органов, ЭХО-КГ)
4. "Лучевая диагностика" (МРТ, КТ, Рентген, Маммография)
5. "Функциональная диагностика" (ЭКГ, Холтер, СМАД, ЭЭГ, Спирометрия)
6. "Эндоскопия" (ФГДС, Колоноскопия)
7. "Процедуры и манипуляции" (Уколы, капельницы, перевязки)
8. "Хирургические операции"
9. "Физиотерапия и массаж"
10. "Стоматология"
11. "Прочее"

Правила:
1. Первичные и повторные консультации - это РАЗНЫЕ услуги, не смешивай их.
2. Сохраняй оригинальный код услуги, если он есть (service_code).
3. standardized_name должно звучать как официальный медицинский термин.

Требуемые поля в JSON для каждой услуги:
- "original_name": Исходное название как в тексте (строка)
- "standardized_name": Красивое, полное, стандартизированное название (строка)
- "service_code": Код услуги (строка или null)
- "price": Цена (строго число, без валюты, или null)
- "category": Строго одна из категорий выше (строка)
- "confidence": Уверенность от 0 до 100 (число). Если аббревиатура очень странная - ставь 70-85.

Пример вывода:
[
  {
    "original_name": "Конс. терапевта повт.",
    "standardized_name": "Повторная консультация терапевта",
    "service_code": "A01.02",
    "price": 8000,
    "category": "Консультации специалистов",
    "confidence": 98
  },
  {
    "original_name": "ЭКГ (снятие+расшифр.)",
    "standardized_name": "Электрокардиография с расшифровкой",
    "service_code": null,
    "price": 4500,
    "category": "Функциональная диагностика",
    "confidence": 100
  }
]"""
    
    response = client.chat.completions.create(
        messages=[
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": f"Сырой текст прайс-листа:\n\n{text_chunk}"}
        ],
        model="llama-3.3-70b-versatile",
        temperature=0.0
    )
    
    result_text = response.choices[0].message.content
    
    try:
        # Ищем начало массива
        start_idx = result_text.find('[')
        end_idx = result_text.rfind(']')
        if start_idx != -1 and end_idx != -1:
            json_str = result_text[start_idx:end_idx+1]
            data = json.loads(json_str)
            return data
        else:
            return []
    except Exception as e:
        print(f"Ошибка парсинга JSON: {e}")
        print("Сырой ответ:", result_text)
        return []
